namespace CDS.ImageDisplay.WinForms.Overlays;

/// <summary>
/// The standard message types for a text panel.
/// </summary>
public enum TextPanelStdMsgTypes
{
    /// <summary>
    /// A title message.
    /// </summary>
    Title,

    /// <summary>
    /// An informational message.
    /// </summary>
    Info,

    /// <summary>
    /// A success message.
    /// </summary>
    Success,

    /// <summary>
    /// A warning message.
    /// </summary>
    Warning,

    /// <summary>
    /// An error message.
    /// </summary>
    Error,
}
